namespace RandomVectorMap.Mapping;

/// <summary>
/// Enumeration of the possible biomes.
/// </summary>
public enum Biome
{
    /// <summary>
    /// No biome is defined.
    /// </summary>
    Undefined,

    /// <summary>
    /// Unpassable salt water.
    /// </summary>
    Ocean,

    /// <summary>
    /// Unpassable fresh water.
    /// </summary>
    Lake,

    /// <summary>
    /// A dry desert.
    /// </summary>
    Desert,

    /// <summary>
    /// Fertile pastures.
    /// </summary>
    Pasture,

    /// <summary>
    /// A thick and tretcherous swamp or mangrove.
    /// </summary>
    Swamp,

    /// <summary>
    /// A lush forest.
    /// </summary>
    Forest,

    /// <summary>
    /// A steep mountain-side.
    /// </summary>
    Mountain,

    /// <summary>
    /// A cold and icy mountain peak.
    /// </summary>
    Snow,
}
