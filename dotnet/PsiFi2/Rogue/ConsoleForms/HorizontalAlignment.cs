namespace Rogue.ConsoleForms
{
    /// <summary>
    /// The horizontal alignment of a control's content.
    /// </summary>
    public enum HorizontalAlignment
    {
        /// <summary>
        /// The left-most content is aligned with the left of the control.
        /// </summary>
        Left,

        /// <summary>
        /// The content is centered within the control.
        /// </summary>
        Center,

        /// <summary>
        /// The right-most content is aligned with the right of the control.
        /// </summary>
        Right,
    }
}
